// 15. Berilgan baho (0-100) asosida A, B, C, D yoki F natijasini chiqaring.

const baho = 70;

if (baho >= 90) {
    console.log("A");
} else if (baho >= 75) {
    console.log("B");
} else if (baho >= 60) {
    console.log("C");
} else if (baho >= 40) {
    console.log("D");
} else {
    console.log("F");
}